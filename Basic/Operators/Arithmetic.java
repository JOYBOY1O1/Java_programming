package Operators;

/*
OPERATORS in Java
Arithmetic Operators
(+,-,*,/,%)
eg:
    operand operator operand = result
        4       +       4    =      8
*/
public class Arithmetic {
    public static void main(String[] args) {
        int n1 = 100, n2 = 20;
        System.out.println("n1 + n2 = " +(n1 + n2));
        System.out.println("n1 - n2 = " +(n1 - n2));
        System.out.println("n1 * n2 = " +(n1 * n2));
        System.out.println("n1 / n2 = " +(n1 / n2));
        System.out.println("n1 % n2 = " +(n1 % n2));
    }

}
