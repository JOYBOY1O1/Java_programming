package Loops;

public class For {
    public static void main(String[] args) {
        /*
         * For Loop
         * for(statement 1 ; statemnt 2 ; statement 3)
         * (initial ; condtiton ; update)
         */

        for (int i = 0; i < 70; i++) {
            System.out.println(i);
        }
    }
}
