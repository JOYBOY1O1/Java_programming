package Strings;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("\nTo print double codes in String use \\\"");
        System.out.println("To print slash in String use \\\\");
        System.out.println("To print slash in String use \\\\");
        System.out.println("\\n \nfor the next line");
        System.out.println("Gives a Tab \t Space \\t ");
    }
}
